package length;

public class Principal {

	public static void main(String[] args) {

		
		String nome = "Alexandro";
		int tamanho = nome.length(); //nome.length() representa o tamanho de uma string.
		System.out.println(tamanho);
		
		int[] megaSena = new int[3];
		megaSena[0] = 3;
		megaSena[1] = 8;
		megaSena[2] = 10;
		
		int tam = megaSena.length; //megaSena.length representa o tamanho de uma lista.
		System.out.println(tam);
	
		
	}

}
