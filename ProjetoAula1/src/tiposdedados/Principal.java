package tiposdedados;

public class Principal {
	public static void main(String[]args) {
		//Classe String
		String nome = "Mario"; //4 bytes
		
		//Tipos primitivos numéricos
		byte a = 1;			//1byte
		short b = 2; 		//2bytes 
		int c = 22; 		//4bytes
		float d = 2.3f; 	//4bytes
		long e = 22; 		//8bytes
		double f = 2.4d; 	//8bytes é mais preciso e recomendável de se utilizar ao se programar em java.
		
		//Tipo primitivo caractere
		char letra = 'a'; //1byte
		
		//Tipo primitivo boolean
		boolean verdadeiroOuFalso = true; //2bytes
		
	}
}
